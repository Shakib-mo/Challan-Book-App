package shakibk.app.challanbook.object;
public class DtoShopInfo {

    public String shopName;
    public String shopMobileNo;
    public String shopEmail;
    public String shopAddress;
    public String shopCity;
    public String shopState;
    public String shopStreet;
    public String shopPostCode;

    public DtoShopInfo(){

    }


    public String getShopName() {
        return shopName;
    }
    public void setShopName(String shopName) {
        this.shopName = shopName;
    }
    public String getShopAddress() {
        return shopAddress;
    }
    public void setShopAddress(String shopAddress) {
        this.shopAddress = shopAddress;
    }
    public String getShopStreet() {
        return shopStreet;
    }
    public void setShopStreet(String shopStreet) {
        this.shopStreet = shopStreet;
    }
    public String getShopCity() {
        return shopCity;
    }
    public void setShopCity(String shopCity) {
        this.shopCity = shopCity;
    }
    public String getShopState() {
        return shopState;
    }
    public void setShopState(String shopState) {
        this.shopState = shopState;
    }
    public String getShopPostCode() {
        return shopPostCode;
    }
    public void setShopPostCode(String shopPostCode) {
        this.shopPostCode = shopPostCode;
    }
    public String getShopMobileNo() {
        return shopMobileNo;
    }
    public void setShopMobileNo(String shopMobileNo) {
        this.shopMobileNo = shopMobileNo;
    }
    public String getShopEmail() {
        return shopEmail;
    }
    public void setShopEmail(String shopEmail) {
        this.shopEmail = shopEmail;
    }


}
